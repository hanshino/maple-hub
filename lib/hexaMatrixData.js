// Helper function to get cost for a specific level and core type
export function getLevelCost(level, coreType) {
  const costData = HEXA_MATRIX_LEVEL_COSTS.find(cost => cost.level === level);
  if (!costData) return null;
  return costData[coreType];
}

// Helper function to calculate total cost up to a level
export function calculateTotalCostUpToLevel(level, coreType) {
  let totalSoulElder = 0;
  let totalSoulElderFragment = 0;

  for (let i = 1; i <= level; i++) {
    const cost = getLevelCost(i, coreType);
    if (cost) {
      totalSoulElder += cost.soul_elder;
      totalSoulElderFragment += cost.soul_elder_fragment;
    }
  }

  return {
    soul_elder: totalSoulElder,
    soul_elder_fragment: totalSoulElderFragment,
  };
}

// Helper function to calculate total cost for max level (30)
export function getMaxLevelTotalCost(coreType) {
  return calculateTotalCostUpToLevel(30, coreType);
}
